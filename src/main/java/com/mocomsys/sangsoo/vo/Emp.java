package com.mocomsys.sangsoo.vo;
//wts
public class Emp {
	
	private Integer emp_UID;
	private String emp_ID;
	private Integer emp_level;
	private String emp_name;
	private Integer dept_UID;
	
	public Integer getEmp_UID() {
		return emp_UID;
	}
	public void setEmp_UID(Integer emp_UID) {
		this.emp_UID = emp_UID;
	}
	
	public String getEmp_ID() {
		return emp_ID;
	}
	public void setEmp_ID(String emp_ID) {
		this.emp_ID = emp_ID;
	}
	public Integer getEmp_level() {
		return emp_level;
	}
	public void setEmp_level(Integer emp_level) {
		this.emp_level = emp_level;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public Integer getDept_UID() {
		return dept_UID;
	}
	public void setDept_UID(Integer dept_UID) {
		this.dept_UID = dept_UID;
	}
	
}
